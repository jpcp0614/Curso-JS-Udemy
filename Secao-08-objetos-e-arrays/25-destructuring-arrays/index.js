/*

* Podemos dividir varáveis com valores de um array com uma notação diferente

*/

let names = ['John', 'Paul', 'George', 'Ringo'];

let [nameA, nameB, nameC, nameD] = names;

console.log(`Beatles: ${nameA}, ${nameB}, ${nameC} e ${nameD}`);

let numbers = [1, 2, 3, 4];

let [num1, num2, num3, num4] = numbers;

console.log(`Números: ${num1}, ${num2}, ${num3} e ${num4}`);