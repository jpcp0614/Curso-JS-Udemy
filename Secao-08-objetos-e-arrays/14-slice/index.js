let numbers = [1, 2, 3, 4, 5, 6, 3];

//* o slice retorna um array a partir de outro array
//* o array retornado é determinado pelos parâmetros que são
//* o primeiro índice e o fim do novo array

console.log(numbers.slice(0, 5)); //* retorna um array com os elementos do índice 0 até o índice 5-1 (4)
console.log(numbers.slice(4)); //* retorna um array com os elementos do índice 4 até o final do array
